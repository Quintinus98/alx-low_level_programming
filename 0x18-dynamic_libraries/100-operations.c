/**
 * add - Adds two numbers
 * @a: Integer variable
 * @b: Integer variable
 *
 * Return: Adds a and b.
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts
 * @a: Integer variable
 * @b: Integer variable
 *
 * Return: Subtracts a and b.
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two numbers
 * @a: Integer variable
 * @b: Integer variable
 *
 * Return: Multiplies a and b.
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides two numbers
 * @a: Integer variable
 * @b: Integer variable
 *
 * Return: Multiplies a and b.
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Modulus two numbers
 * @a: Integer variable
 * @b: Integer variable
 *
 * Return: Multiplies a and b.
 */

int mod(int a, int b)
{
	return (a % b);
}
